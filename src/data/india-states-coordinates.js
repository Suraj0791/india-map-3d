// Helper function to normalize coordinates
export const normalizeCoordinates = (coordinates, scale = 100) => {
  // Find the bounds of all coordinates across India
  const bounds = {
    minX: 68.18, // Westernmost point (Gujarat)
    maxX: 84.23, // Easternmost point (Andhra Pradesh)
    minY: 12.78, // Southernmost point (Karnataka)
    maxY: 24.68, // Northernmost point (Gujarat)
  };

  // Normalize to -1 to 1 range with proper aspect ratio
  const width = bounds.maxX - bounds.minX;
  const height = bounds.maxY - bounds.minY;
  const aspectRatio = width / height;

  return coordinates.map(([x, y]) => [
    ((x - bounds.minX) / width - 0.5) * scale * aspectRatio,
    ((y - bounds.minY) / height - 0.5) * scale,
  ]);
};
